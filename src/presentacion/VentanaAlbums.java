    private void botonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarActionPerformed
        String Clave = campoClave.getText();
        String Nombre = campoTitulo.getText();
        String Disquera = campoDisquera.getText();

        albums.setCveAlbum(Clave);
        albums.setTitulo(Nombre);
        albums.setDisquera(Disquera);
        albums.setFecha(new Date(campoFecha.getText()));

        try {
            jpaGeneros.create(albums);
            campoClave.setText(null);
            campoTitulo.setText(null);
            campoDisquera.setText(null);
            campoFecha.setText(null);
            setModel(tablaAlbums);
            mostrarAlbums();
            JOptionPane.showMessageDialog(null, "Album agregado correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaAlbums.class.getName()).log(Level.SEVERE, null, e);
        }


    }//GEN-LAST:event_botonRegistrarActionPerformed

    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        String Clave = campoClave.getText();
        String Nombre = campoTitulo.getText();
        String Disquera = campoDisquera.getText();

        albums.setCveAlbum(Clave);
        albums.setTitulo(Nombre);
        albums.setDisquera(Disquera);
        albums.setFecha(new Date(campoFecha.getText()));

        try {
            jpaGeneros.edit(albums);

            setModel(tablaAlbums);
            mostrarAlbums();
            JOptionPane.showMessageDialog(null, "Album actualizado correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaActor.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

