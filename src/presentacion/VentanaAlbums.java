    private void tablaAlbumsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAlbumsMousePressed
        int fila = tablaAlbums.getSelectedRow();
        if (fila >= 0) {
            campoClave.setText(tablaAlbums.getValueAt(fila, 0).toString());
            campoTitulo.setText(tablaAlbums.getValueAt(fila, 1).toString());
            campoDisquera.setText(tablaAlbums.getValueAt(fila, 2).toString());
            campoFecha.setText(tablaAlbums.getValueAt(fila, 3).toString());
            botonEliminar.setEnabled(true);
            botonActualizar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "NO A SELECCIONADO NINGUN ALBUM");
        }
    }//GEN-LAST:event_tablaAlbumsMousePressed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        albums.setCveAlbum(campoClave.getText());

        try {
            jpaGeneros.destroy(campoClave.getText());
            setModel(tablaAlbums);
            mostrarAlbums();
            campoClave.setText(null);
            campoTitulo.setText(null);
            campoDisquera.setText(null);
            JOptionPane.showMessageDialog(null, "Album eliminado correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaActor.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

