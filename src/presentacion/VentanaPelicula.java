    private void mostrarPeliculas() {
        setModel(tablaPeliculas);
        DefaultTableModel modelo = (DefaultTableModel) tablaPeliculas.getModel();
        try {
            jpaPeliculas.insertarModelo(modelo);
        } catch (Exception e) {
            Logger.getLogger(VentanaActor.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    private void setModel(JTable table) {
        while (table.getRowCount() > 0) {
            ((DefaultTableModel) table.getModel()).removeRow(0);
        }
    }

