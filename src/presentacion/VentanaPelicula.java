    private void tablaPeliculasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPeliculasMousePressed
        int fila = tablaPeliculas.getSelectedRow();
        if (fila >= 0) {
            campoClave.setText(tablaPeliculas.getValueAt(fila, 0).toString());
            campoDirector.setText(tablaPeliculas.getValueAt(fila, 1).toString());
            campoFecha.setText(tablaPeliculas.getValueAt(fila, 2).toString());
            botonEliminar.setEnabled(true);
            botonActualizar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "NO A SELECCIONADO NINGUNA PELICULA");
        }
    }//GEN-LAST:event_tablaPeliculasMousePressed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        peliculas.setClave(campoClave.getText());

        try {
            jpaPeliculas.destroy(campoClave.getText());
            setModel(tablaPeliculas);
            mostrarPeliculas();
            campoClave.setText(null);
            campoFecha.setText(null);
            campoDirector.setText(null);
            JOptionPane.showMessageDialog(null, "Pelicula eliminada correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaActor.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

