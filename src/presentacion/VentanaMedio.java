    private void tablaGenerosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaGenerosMousePressed
        int fila = tablaGeneros.getSelectedRow();
        if (fila >= 0) {
            campoClaveGenero.setText(tablaGeneros.getValueAt(fila, 0).toString());
        } else {
            JOptionPane.showMessageDialog(null, "NO A SELECCIONADO NINGUN GENERO");
        }
    }//GEN-LAST:event_tablaGenerosMousePressed

    private void tablaMediosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMediosMousePressed
        int fila = tablaMedios.getSelectedRow();
        if (fila >= 0) {
            campoClave.setText(tablaMedios.getValueAt(fila, 0).toString());
            campoTitulo.setText(tablaMedios.getValueAt(fila, 1).toString());
            campoTipoMedio.setText(tablaMedios.getValueAt(fila, 2).toString());
            campoDuracion.setText(tablaMedios.getValueAt(fila, 3).toString());
            botonEliminar.setEnabled(true);
            botonActualizar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "NO A SELECCIONADO NINGUN MEDIO");
        }
    }//GEN-LAST:event_tablaMediosMousePressed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        String clave = campoClave.getText();

        medio.setClave(clave);

        try {
            jpaMedios.destroy(campoClave.getText());
            setModel(tablaMedios);
            mostrarMedios();
            campoClave.setText(null);
            campoTitulo.setText(null);
            campoTipoMedio.setText(null);
            campoDuracion.setText(null);
            campoClaveGenero.setText(null);
            JOptionPane.showMessageDialog(null, "Medio eliminado correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaActor.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

