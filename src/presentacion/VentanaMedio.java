    private void botonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarActionPerformed
        String claveGenero = campoClaveGenero.getText();
        String Clave = campoClave.getText();
        String Titulo = campoTitulo.getText();
        String TipoMedio = campoTipoMedio.getText();
        int Duracion = Integer.parseInt(campoDuracion.getText());
        genero.setCveGenero(claveGenero);

        medio.setClave(Clave);
        medio.setTitutlo(Titulo);
        medio.setTipoMedio(TipoMedio);
        medio.setDuracion(Duracion);
        medio.setCveGenero(genero);
        try {
            jpaMedios.create(medio);
            setModel(tablaGeneros);
            mostrarGeneros();
            campoClave.setText(null);
            campoTitulo.setText(null);
            campoTipoMedio.setText(null);
            campoDuracion.setText(null);
            campoClaveGenero.setText(null);
            JOptionPane.showMessageDialog(null, "Medio agregado correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaMedio.class.getName()).log(Level.SEVERE, null, e);
        }

    }//GEN-LAST:event_botonRegistrarActionPerformed

    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        String claveGenero = campoClaveGenero.getText();
        genero.setCveGenero(claveGenero);
        medio.setClave(campoClave.getText());
        medio.setTitutlo(campoTitulo.getText());
        medio.setTipoMedio(campoTipoMedio.getText());
        medio.setDuracion(Integer.parseInt(campoDuracion.getText()));
        medio.setCveGenero(genero);
        
        try {
            jpaMedios.edit(medio);
            campoClave.setText(null);
            campoTitulo.setText(null);
            campoTipoMedio.setText(null);
            campoDuracion.setText(null);
            campoClaveGenero.setText(null);
            setModel(tablaGeneros);
            mostrarGeneros();
            JOptionPane.showMessageDialog(null, "Medio actualizado correctamente");
        } catch (Exception e) {
            Logger.getLogger(VentanaActor.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

